#include "../../../cub3d.h"

char	*ft_strdup(char *s1)
{
	char	*s2;
	size_t	i;

	s2 = malloc(sizeof(char) * (ft_strlen (s1) + 1));
	if (!s2)
		return (0);
	i = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
